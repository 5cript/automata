#include "trigger.hpp"

namespace MiniAutomata
{
//#####################################################################################################################
    Trigger::Trigger(std::function <bool()> const& condition)
        : condition_{condition}
    {

    }
//---------------------------------------------------------------------------------------------------------------------
    Trigger::Trigger()
        : condition_{[](){return true;}}
    {

    }
//---------------------------------------------------------------------------------------------------------------------
    void Trigger::setCondition(std::function <bool()> const& condition)
    {
        condition_ = condition;
    }
//---------------------------------------------------------------------------------------------------------------------
    bool Trigger::test() const
    {
        return condition_();
    }
//#####################################################################################################################
}
